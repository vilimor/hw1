<?php
//Създавам for цикъл, който генерира числата от 1 до 10 вкл.
for($i=1;$i<11;$i++)
{
    //извеждам числата
    echo $i;
    //създавам while, който казва, че докато числото е по-малко от  10, да се извежда - между всяко число
    while( $i < 10) 
    {
        echo '-';
        //прекратявам while цикъла.
        break;
        
    }
    
   
    
}